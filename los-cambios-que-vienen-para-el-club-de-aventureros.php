        <h5>LOS CAMBIOS QUE VIENEN PARA EL CLUB DE AVENTUREROS A NIVEL MUNDIAL</h5>
        <figure class="img-box"><img width="720px" height="540px" src="images/noticias/libro_marca_aventureros.png" ></figure>
        <p>
        Hay distintas modificaciones que se implementaran para el club de aventureros a nivel mundial segun los últimos
        acuerdos votados en la asociación general. Te contamos todos los detalles.
        </p>
        <p>
        <h6>Nuevo Logo</h6>
        Este ha sido uno de los temas mas comentados en las redes sociales en el último tiempo. Segun lo expresado
        por el equipo de coordinacion de la Asociación General en palabras del Pr Jonatan Tejel "había una preocupación
        porque el logo de aventuereros que todos usabamos no transmitía necesariamente la filosofia de este minsiterio.
        Si uno observa los logos de Conquistadores y JA es capaz de percibir con claridad un mensaje", no así con los
        aventureros que solo se aparecian los logos de cada clase progresiva.Con esto en mente el nuevo logo trata de resumir con claridad la filosofia del club de aventureros. Se puede apreciar
        claramente a una familia, llevando a sus hijos hacia Jesús(representado en la cruz) todo esto en medio de la naturaleza.
        </p>
        <p>
        <h6>¿Se oficialia el Club de Castorcitos?</h6>
        Los castorcitos es un programa que segun nuestros registros se inició en Paraguay como una iniciativa laica hace algunos años. La verdad es que ver a los pequeños de pañolin verde nos recordó enseguida al origen del Club de Aventureros cuando los cachorros acompañaban a sus hermanos conquistadores al club.
        <br><br>
        En el último tiempo la iglesia ha estado revisando este tema, si bien en varios lugares del continente existió la indicación
        de que Castorcitos no es un programa oficial de la iglesia y por ende no debía funcionar, no es menos cierto que el interes de los padres por que los pequeños participen del programa no ha decaído, sino al contrario.
        Con esto en mente, <b>la Iglesia ha nivel mundial ha votado que la inclusión de dos nuevas clases como parte del programa del club de aventuereros, que serán Corderitos(4 años) y Castorcitos(5 años)</b> con lo que el programa del club de aventureros pasará de un total de 4 a 6 clases. Algunos detalles importantes sobre la puesta en marcha 
        de estas modificaciones:
        <br><br>
        <ul>
            <li>1.-Se estima que el material oficial para poder comenzar a trabajar con las nuevas clases estará listo dentro de un año</li>
            <li>2.-A partir de la implementación del cambio las clases de Corderitos, Castorcitos y Abejitas laboriosas la presencia de los padres durante el programa regular es Obligatoria</li>
            <li>3.-La Asociacion General ha indicado que si bien no es para ser implementado ahora ya en cada continente, si es algo que deben aplicar todos los campos en el corto/mediano plazo</li>
        </ul>
        </p>
        <p>
        <h6>Pañolin</h6>
        <p>
        Continuando con la busqueda de uniformidad a nivel mundial del ministerio, se ha votado la decisión de usar un solo pañolin para todos los
        aventureros del planeta.El color escogido corresponde a Burdeo.
        </p>
        <h6>Logo para los GM</h6>
        <p>
        Esto ha trascendido pero no tenemos hasta ahora una voz oficial al respecto. Se comenta que el logo de GM de Aventureros pasará a ser el mismo
        que el que actualmente usan los conquistadores(hexagono azul).                
        </p>
        <h6>Implementación en la Divisón Sudamericana</h6>
        <p>
        Todo cambio toma tiempo para ser implementado, sobre todo cuando esas modificaciones afectan a todos los clubes del mundo y muchos de ellos
        han invertido recursos en materiales, unformes, etc. Así como cuando se produjo el cambio del primer logo al que usabamos hasta ahora.
        Hay que considerar además los contextos de cultura, analisis del ministerio joven de cada continente, traducción y adaptación de materiales, etc. En lo personal
        creo que de implemetarse todas estas modificaciones es dificil que ocurra antes de 5 años.
        </p>
        <p>
        De todas maneras al menos sobre la implementacion de las clases de castorcitos y corderitos la Divisón Sudamericana ya ha marcado su posición
        al respecto llamando <b>a no incentivar esto por ahora</b>. Mas detalles en el siguiente link:
        <br><br>        
        <a href="http://downloads.adventistas.org/es/iasd-counter-link/MTU2ODA%3D" target="_blank">http://downloads.adventistas.org/es/iasd-counter-link/MTU2ODA%3D</a>
        </p>
        <p>
        Lo que sin duda nos alegra es que por la gracia de Dios la iglesia a nivel mundial ha volcado sus esfuerzos a invertir tiempo en evaluar como
        se está trabajando y mejorar lo que sea necesario. Dios ha despertado en su pueblo la conciencia a que es sumamente importante trabajar
        con este grupo etareo para asi poder evitar que se alejen de la iglesia en la adolescencia(que es lo que indican esta ocurriendo en la actualidad)
        </p>
