         asiático. Cerca de 10 niños entre 6 y 9 años participan regularmente de las reuniones, que se llevan a cabo 
         tres veces por mes, los sábados y domingos, y que contemplan el desarrollo físico, mental y espiritual de 
         manera equilibrada."</i> 
        </p>
