         a los mas pequeños con un programa especial para ellos. Segun menciona el portal En una de sus primeras reuniones, los aventureros 
         realizaron un servicio comunitario limpiando la orilla del río Tuul, de la capital del país. 
        </p>
        <p>
         <b>Gabriela Grassi</b>, misionera de origen Brasileño expresó <i>"Estar involucrado con los niños en sus años de desarrollo es una gran 
