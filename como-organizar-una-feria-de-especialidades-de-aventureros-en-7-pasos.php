        <h5><b>Como Organizar una Feria de Especialidades de Aventureros en 7 pasos</b></h5>
        <p><img class="img-box" src="images/recursos/guia_feria.png" /></p>
        <p class="contenido-recursos">
         El Manual de Aventureros para la división sudamericana establece un total de 53 Especialidades, <a href="https://www.adventurer-club.org/adventurer-awards/overview" target="_blank">si a eso le sumamos las especialidades publicadas en ingles por el ministerio joven de la asociación general</a> el numero supera facilmente las 100. 
        </p>
        <p class="contenido-recursos">
          En mi experiencia, un aventurero que haya estado 4 años en el club puede alcanzar unas 30 especialidades en el mejor de los casos, la mayoria de ellas las soclicitadas por las clases progresivas y ¿Que hay del resto?, las ferias de especialidades son una buena oportunidad para alcanzar nuevos honores.
        </p>
        <p class="contenido-recursos">
          <span><b>¿Que es una feria de especialidades?</b></span><br>
          Como antes nos contó la GM Nalva Martins, <a href="http://www.somosaventureros.cl/como-organizar-una-feria-de-especialidades-de-aventureros.php" target="_blank">en un articulo muy interesante</a>, La feria es un programa que generalmente tiene un día de duración en donde el enfasis principal esta en que los niños puedan alcanzar nuevas especialidades, es decir, se enfoca principalmente en que los niños reciban instrucción sobre temas específicos, puede ser organizada por un club para sus integrantes o incluso que ese club invite a otros clubes tambien a participar, tambien es posible que el Distrito, Zona o Asociación o Misión preparen el programa. ¿Te gustaría saber como organizar una feria?,  sigue leyendo porque ahora vamos con una guía paso a paso para conseguirlo.
        </p>
        <p class="contenido-recursos">
          <span><b>1.- Planificar el Evento</b></span><br>
          Como cualquier actividad que el ser humano realiza requiere tiempo de sentarse a conversar y definir los objetivos que se desean alcanzar, las ideas en el aire no suelen dar muy buenos resultados,  asi que toma tu computador o simplemente papel y lapiz, reune a tu equipo de trabajo y comienza a definir las ideas de tu programa. Aqui algunas cosas que debería incluir tu planificación.
          <ul class="contenido-recursos">
            <li>a) Objetivos(que esperamos alcanzar con el programa, en otras palabras porque hacemos la feria)</li>
            <li>b)Fecha</li>
            <li>c)Duracion del programa</li>
            <li>d)Lugar en que se realizará el evento</li>
            <li>e)Quienes pueden participar(tu club, otros clubes, etc)</li>
            <li>f)Cuantas y cuales especialidades se dictaran(idealmente deben ser especialidades que los niños no harán como parte de las clases progresivas)</li>
            <li>g)Cuantos cupos habrá por cada especialidad(idealmente no deberian haber mas de 12 niños por especialidad)</li>
            <li>h)Quienes serán los instructores</li>
            <li>i)Cual será el presupuesto(considerar arriendo del lugar, parches para los niños, certificados, alimentación, escenografia, etc)</li>
            <li>j)Con el presupuesto hecho definir el valor de inscripción</li>
            <li>Forma de Inscripción y plazos de inscripción(en persona, via internet, etc)</li>
            <li>k)Definir las comisiones de trabajo y responsables de cada una(promoción, escenografía, conseguir los instructores, alimentacion, inscripciones, compra de materiales, etc) </li>
          </ul>
        </p>
        <p class="contenido-recursos">
          <span><b>2.- Formalizando con la Junta de Iglesia</b></span><br>
          Antes de siquiera contarle a alguien sobre esta idea debes validar tu proyecto con la junta de tu iglesia, tener el apoyo y respaldo de los lideres de tu iglesia no solo es lo correcto, es lo mejor para garantizar el exito de tu evento. Ya se que muchas veces los hermanos no entienden las locas ideas de los lideres de aventureros, pero tu haz lo correcto que Dios bendecirá tus esfuerzo, busca apoyo, involucra a tu pastor con alguna meditación, pide apoyo al departamento de comunicaciones para saber como difundir la actividad, no actues como una isla, el club no lo es, somos parte de un cuerpo, la iglesia apreciará ver un plan de trabajo tan ordenado y puede que si lo necesitas hasta te apoyen con recuersos financieros para hacer que la actividad sea de bendición para todos.
        </p>
        <p class="contenido-recursos">
          <span><b>3.- Inicio del Trabajo</b></span><br>
          Ahora que ya esta todo en orden, es el momento de comenzar el trabajo. Cada persona responsable de as comisiones deberá ponerse a trabajar en sus responsabilidades, es muy importante que cada uno cumpla con
          las labores para asi garantizar el exito de la actividad. Mientras el equipo de publicidad promociona el evento, ya el encargado de coordinar con los instructores debe estar viendo todos los detalles, de la misma forma cada persona del equipo cumpliendo su labor.
        </p>
        <p class="contenido-recursos">
          <span><b>4.- Revisión de Avance</b></span><br>
          Es bueno realizar periodicamente revisiones de avance de cada una de las comisiones, ante cualquier eventualidad los encargados deberian informar al director las situaciones que no puedan resolver, para asi
          nada impida el buen funcionamiento del proyecto. Te sugiero que hagas al menos una reunión 1 mes antes y luego una semana antes del evento para ir haciendo seguimiento de los detalles.
        </p>
        <p class="contenido-recursos">
          <span><b>5.- Cierre de Inscrpiciones</b></span><br>
        El cierre de inscripciones debería ocurrir como mínimo 15 días antes de la fecha de la feria, ¿porque?, pues porque tu deberas preparar los materiales, los parches, almuerzos y muchos otros detalles y para eso debes saber realmente cuantas personas van a participar, es por eso que te recomiendo que las inscripciones sean siempre con el pago de la inscripción o al menos el 60% del valor total, aunque como las ferias son eventos que no tienen inscripcions muy caras lo mejor sería que pagaran los asistentes pagaran la inscripción completa de una vez, pues en caso de que las personas no asistan a ultima hora eso puede afectar tu presupuesto, esto no se trata necesariamente de reunir dinero para el club, pero tampoco perderlo.
        </p>
        <p class="contenido-recursos">
          <span><b>6.- El día Esperado</b></span><br>
        ¡Llegó el día!, lo que estuvieron preparando con tu equipo es una realiad, las personas ya comienzan a detallar, tu estas pendiente de todos los detalles, tienes que saber que habrá imprevistos que resolver, algunas de las cosas no saldrán como tu querías, pero si seguiste los pasos 1 al 5 serán muy pocas y creeme esas cosas las notas solo los organizadores, con la ayuda de Dios todos irán contentos a casa, asi que ahora disfruta de tu feria de especialidades y agradece a Dios por permitirte impactar la vida de los niños.
        </p>
        <p class="contenido-recursos">
          <span><b>7.- Evaluación</b></span><br>
        Esto es algo que se suele hacer poco, pero es uno de los pasos mas importantes, ¿Se cumplieron los objetivos?, ¿Que cosas fallaron?, ¿Que se puede hacer para que la proxima version de tu feria no repita esos fallos?, Toma nota de todos estos detalles y revisenlos al año siguiente antes de empezar a trabajar en una nueva versión de la feria.
        </p>
        <p class="contenido-recursos">
          <span><b>CONCLUSIONES</b></span><br>
        Hemos llegado al final de esta guía, las ferias de especialidades pueden ser un programa de mucha bendición para quienes participan, pero sobre todo para quienes lo organizan, ¿Que esperas para empezar a planificar tu propia feria de especialidades?. Para darte una manito adicional te comparto el <a target="_blank" href='https://drive.google.com/file/d/1PxsmOhqDZssd-9oMu_fV8N1gdW_JqNA3/view?usp=sharing'>manual de orientaciones</a> de la ultima Feria de Especialidades del <a href="https://www.facebook.com/profile.php?id=100009162714091&ref=br_rs" target="_blank">Club Neftalí</a> en Chile, quienes ya llevan 3 ediciones del programa, muchas gracias al director del club <strong>Manuel Tapia</strong> por compartir este valioso material.
        </p>

        
        
