                <p>
                  Una feria de especialidades es un evento de un día donde los aventureros tienen la oportunidad de cumplir con los requisitos de una manera objetiva, a través de talleres con instructores preparados para guiarlos.</p>
                  <p><strong>Objetivo:</strong> El objetivo principal de este evento es hacer que los aventureros ganen más especialidades y también adquieran más conocimientos, contenidos y disciplinas a través de las enseñanzas que se entregaran en los talleres.</p>
                  <p><strong>¿Cómo hacer una feria de especialidades? :</strong> La feria  se puede hacer en su club, distrito o zona.</p>

                <p>Cuanto mas aventureros participen, más atractivo será el evento. Primero elija las especialidades que se trabajaran, luego invita a  instructores preparados dentro de cada área. Por ejemplo, si elige estas especialidades:</p>
                <p>Arte Culinario, invitar a un nutricionista o gastrónomo, cocinar, etc.</p>
                <p>Primeros auxilios puede  invitar a una enfermera o un médico. Así se asegurará les que sus aventureros tendrán clases prácticas de calidad.</p>
                <p>Recuerde que el número de especialidad debe ser igual al número de clubes o grupos para que la feria este bien organizada y  no se produzcan atochamientos..</p>
                <p>Es importante proporcionar materiales que se utilizarán en los talleres y un lugar adecuado para llevar a sus aventueros. Por ejemplo, se recomienda  una escuela, ya que tiene escenario para que usted haga la apertura del evento, con los salones de clase para los talleres. Usted puede preparar parches del evento y trofeos para los clubes que participaron, premios , etc.</p>
                <p>En nuestro manual actual, contamos con 53 especialidades, puede elegir las  que no están colocadas en las clases progresivas. Los grupos o clubes participarán de forma simultánea  en los talleres. Las especialidades que no se pueden cumplir al 100 pueden dejar actividades de tarea que ellos pueden completar em sus reuniones reguares. Les puedo asegurar que a todos los aventureros les encantará !! Además de la diversión, No hay mejor manera de aprender para que un niño</p>
