        <h5>OMA - ORIENTACIÓN SOBRE LOS LIDERES DE AVENTUREROS MENORES DE 18 AÑOS</h5>
        <p>
         La divisón sudamericana ha publicado el primer articulo sobre orientacipon para e ministerio de los aventureros
         para nuestro continiente. Aqui el detalle:
        </p>
        <p> <strong>A partir de esta fecha oficializamos que:</strong></p>
        <ul>
          <li> -El Director del Club de Aventureros debe tener 18 años o
          más.</li>
          <li> -Los cargos de Consejeros de Unidad y otras funciones de
          liderazgo en el Club pueden ejercerlas personas con 16 años
          o más.</li>
          </ul>
          <br>
          <p><strong>Siendo así:</strong></p>
           <ul > 
          <li>-El Director del Club es el responsable legal de los
          jovencitos cuando están en actividades oficiales:
          reuniones, campamentos y proyectos en unidades o en
          general.</li>
          <li> -El ideal es que la mayoría de los líderes del Club
          (Directores asociados, secretaria, instructores y
          consejeros de unidad sean mayores de edad. Pero
          cuando faltan personas con más experiencia, el Director
          es el líder con responsabilidad para representar el Club
          delante de las autoridades.</li>
          <li> -Ninguna actividad del Club debe desarrollarse sin la
          presencia de un adulto (mayor de edad). Cualquier
          actividad externa realizada por una Unidad o Club solo
          se hará efectiva con la presencia de un líder mayor de
          edad. Si, por ejemplo, una Unidad tiene un consejero
          con edad entre 16 y 17 años, la unidad solo podrá hacer
          actividades en plazas, campamentos, proyectos
          comunitarios, caminatas, etc., con la presencia de
          alguien de la dirección mayor de edad.</li>
        </ul> 
