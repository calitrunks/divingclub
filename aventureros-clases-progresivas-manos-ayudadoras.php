      <h5>MANOS AYUDADORAS</h5>
      <p>La Clase Manos Ayudadoras, esta orientada a los niños de 9 años de edad. Comprende el trabajo de un año con los siguiente requisitos.</p>

		<h6>I. REQUISITOS BASICOS</h6>
		<p>a) Memorizar el voto y la ley de los Aventureros.</p>
		<p>b) Explicar la ley del Aventurero.</p>
		<p>c)  Leer el libro del curso de lectura de los Aventureros.</p>


		<h6>II. MI DIOS</h6>
		<p><strong>1.- Su plan para salvarme:</strong></p>
		<p>a) Crear un cuadro mural mostrando el orden en que sucedieron estas historias: -Noe -Abraham -Moisés -David -Daniel </p>
		O las historias de la Biblia que están estudiando en la clase o Escuela Sabática.
		<p>b) Preparar una canción, o una poesía, o una maqueta, con masa de modelar, sobre una de las historias del requisito anterior, demostrando a alguien cómo vivir por Jesús.</p>
		<p><strong>2.- Su mensaje para mí:</strong> </p>
		<p>a) Encontrar, memorizar y explicar 3 versículos de la Biblia que hablen sobre vivir por Jesús.</p>
		- Exo. 20:12-17 – Filip.2:13 – Filip. 4:13 – 1 Juan 2:1,2 – Judas 24
		<p>b)  Recitar en orden los libros del Antiguo Testamento.</p>
		<p><strong>3.- Su poder en mi vida </strong></p>
		<p>a) Pasar un tiempo regular en contacto con Jesús, conversando con él y aprendiendo sobre él. </p>
		<p>b) Juntamente con un adulto, escoger alguna cosa que te gustaría mejorar en tu vida. En seguida orar, planificar y trabajar por eso, buscando la ayuda de Jesús.</p>


		<h6>III. YO MISMO</h6>
		<p><strong>1.- Soy especial:</strong></p>
		<p>a) Hacer una relación de algunos intereses y habilidades especiales que Dios te dio.</p>
		<p>b) Demostrar y compartir tus talentos, haciendo una especialidad que permita expresar tu talendo personal.</p>
		<p><strong>2.- Puedo hacer elecciones sabias: </strong></p>
		<p>a) Aprender los pasos que deben ser dados para hacer una buena elección.</p>
		<p>b) Usar esos pasos para resolver dos problemas en la vida real.</p>
		<p><strong>3.- Puedo cuidar mi cuerpo: </strong></p>
		<p>a) Completar la especialidad de Higiene</p>


		<h6>IV. MI FAMILIA</h6>
		<p><strong>1.- Tengo una familia:</strong></p>
		<p>a) Escoger una de las siguientes opciones:</p>
		<ul>
    	<li>- Hacer una bandera o emblema de tu familia</li>
     	<li>- Reunir hechos o fotos sobre la historia de tu familia.</li>
     	</ul>
		<p><strong>2.- Los miembros de la familia cuidan unos a otros:</strong></p>
		<p>a) Ayudar a planificar una de las siguientes actividades en familia:</p>
		<ul>
      	<li>- Culto familiar especial</li>
      	<li>- Noche en familia</li>
      	<li>- Paseo.</li>
      	</ul>
		<p><strong>3.- Mi familia me cuida:</strong></p>
		<p>a) Completar una especialidad que aún no haya sido realizada, en una de las siguientes áreas:</p>
		<ul>
       	<li>- Artes</li>
       	<li>- Actividades domésticas</li>
       	<li>- Habilidades.</li>
       	</ul>
		<h6>V. MI MUNDO</h6>
		<p><strong>1.- El mundo de los amigos:</strong></p>
		<p>a) Completar la especialidad de Amigo Gentil.</p>
		<p><strong>2.- El mundo de las demás personas:</strong></p>
		<p>a) Escoger una nacionalidad o cultura para estudiar. Encontrar una manera de compartir el amor de Jesús con alguna persona de la cultura o nacionalidad escogida.</p>
		<p><strong>3.-  El mundo de la naturaleza:</strong></p>
		<p>a) Completar la especialidad de Ecología.</p>


