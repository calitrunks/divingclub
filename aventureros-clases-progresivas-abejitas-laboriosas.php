        <h5>ABEJITAS LABORIOSAS</h5>
        <p>
        La Clase Abejitas Laboriosas, esta orientada a los niños de 6 años de edad. Comprende el trabajo de un año con los siguiente requisitos.</p>
            
            <h6>I. REQUISITOS BASICOS </h6>
            <p>a) Memorizar y aceptar el Voto de los Aventureros.</p>
            <p>b) Refuerzo: Leer el libro del Curso de Lectura de los Aventureros.</p>
            <h6>II. MI DIOS </h6>
            <p><strong>1.- Su plan para Salvarme: </strong></p>
            <p>a) Crear un cuadro mural con las siguientes historias, mostrando el orden en que sucedieron o sucederán:  -La creación – Inicio del pecado y la tristeza  - Jesús me cuida hoy  -Jesús vendrá nuevamente  - El cielo 
            O las historias de la Biblia que están estudiando en la clase o Escuela Sabática.</p>
            <p>b) Hacer un dibujo o hablar sobre una de las historias del requisito anterior, para explicar a alguien cómo nos cuida Jesús.</p>
            <p><strong>2.- Su mensaje para mí:</strong></p>
            <p>a) Completar la especialidad de Biblia I.</p>
            <p><strong>3.- Su poder en mi vida:</strong></p>
            <p>a) Pasar un tiempo regular en contacto con Jesús, conversando con él y aprendiendo sobre él.</p>
            <p>b) Preguntar  a tres personas por qué oran.</p>

            
            <h6>III. YO MISMO </h6>
            
            <p><strong>1.- Soy especial:</strong></p>
            <p>a) Preparar un cuaderno mostrando diferentes personas que te cuidan y se interesan por ti, de la manera en que Jesús lo hace.</p>
            <p><strong>2.- Puedo hacer elecciones sabias:</strong></p>
            <p>a) Practicar el juego de los sentimientos y mencionar por lo menos cuatro sentimientos diferentes.</p>
            <p><strong>3.- Puedo cuidar mi cuerpo:</strong></p>
            <p>a) Completar la especialidad de Cuidado de la Salud.</p>

            
            <h6>IV. MI FAMILIA</h6>
            <p><strong>1.- Tengo una familia:</strong></p>
            <p>a) Hacer una pintura o dibujo de algo que te gusta en cada miembro de tu familia.</p>
            <p><strong>2.- Los Miembros de la familia se cuidan unos a otros:</strong> </p>
            <p>a) Descubrir qué dice el Quinto mandamiento (Éxodo 20:12) sobre las familias.</p>
            <p>b) Demostrar  tres maneras en las que puedes honrar a tu familia.</p>
            <p><strong>3.- Mi familia me cuida</strong></p>

            
            <h6>V. MI MUNDO</h6> 
            <p><strong>1.- El mundo de los amigos:</strong></p>
            <p>a) Explicar cómo puedes ser un buen amigo. Para eso usar títeres, dramas, piezas u otros recursos.</p>
            <p><strong>2.-El mundo de las otras personas:</strong> </p>
            <p>a) Hablar sobre el trabajo que realizan las personas en tu iglesia y descubrir una manera de ayudarlas.</p>
            <p><strong>3.- El mundo de la naturaleza </strong></p>
            <p>a) Completar la especialidad de Amigo de los Animales.</p>
            
