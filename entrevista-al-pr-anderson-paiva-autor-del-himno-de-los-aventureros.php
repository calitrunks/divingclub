        <h5>ENTREVISTA AL PASTOR ANDERSON PAIVA, AUTOR DEL HIMNO DE LOS AVENTUREROS</h5>
        <p>
         <i>Entrevistando al pastor Wanderson Paiva respecto de su composición del Himno de los Aventureros sentí la grandiosidad 
         de este ministerio que nació en el corazón de Dios. Inspiración divina! Dios trabajó en la vida de este hombre, 
         porque ya sabía cuanto crecería este ministerio y sería bendición en la vida de los niños, familia, Iglesia y comunidad.</i>
        </p>
         <p>
          <figure class="img-box"><img width="100%" height="50%" src="images/recursos/himno.gif" ></figure>
        </p>
        <p>
        <b>Pastor nosotros ya sabemos que el Himno Oficial de los Aventireros fue compuesto en 1995 por usted, para la DSA. 
         Antes de componerlo ¿Usted ya estudiaba música? Si es asi ¿Hace cuanto tiempo?</b>
        </p>
        <p>
        Comencé a estudiar música a partir de los 13 años, en la IASP, comencé a estudiar mi Instrumento Principal – Tompeta. 
        Con 19 años terminé como Técnico en Instrumento en el Instituto Adventista de Ensino y con 22 años concluí el Bachillerato
        en Tormpeta en la Universidad de San Judas Tadeo, ambas escuela en Sao Paulo.
        </p>
        <p>
        <b>¿Que edad tenía cuando compuso el Himno de los Aventureros?</b>
        </p>
        <p>
        Según mis cuentas, tenía 28 años.
        </p>
        <p>
        <b>¿Hay mas músicos en su familia?</b>
        </p>
        <p>
        La verdad, en casa,  a todos nos gusta la musica. Mi madre estudió un poco, mi hermana es pianista y todos apreciamos bastante este arte.
        </p>
        <p>
        <b>¿Quien lo inspiró?</b>
        </p>
        <p>
        La inspiración para el ministerio musical vino de la participación en las actividades de la iglesia y asi poder ser útil al Señor. 
        Diversos  han influido en mi trayectoria, tales  como: Williams Costa Jr., Flávio Garcia y otros
        </p>
        <p>
        <b>¿Cómo descubrió este talento?</b>
        </p>
        <p>
        Todos tenemos talentos con los que nacemos y talentos que se transforman en dones cuando son usados por el  Espíritu Santo. El descubrimiento mismo se pone en las manos de Dios, confiando que el transformará  todos los talentos en dones para su gloria. Eso debe ser una entrega diaria.
        </p>
        <p>
        <b>¿A partir de que pensamientos surgió la letra del himno?</b>
        </p>
        <p>
        La letra del Himno de los Aventureros surgió como un resumen de los ideales retratados en la  misión y objetivos del club. Es muy difícil sintetizar tanta cosa buena y bonita en pocas frases. La verdad, pensé en algo que fuese simple, fácil de recordar y con contenido pleno.
        </p>
        <p>
        <b>¿Qué lo llevó a componer el himno, fue a pedido de alguien del Ministerio Joven?</b>
        </p>
        <p>
        Este himno surgió para atender las necesidades del club de Aventureros del IAP – Instituto Adventista Paranaense. El Club del IAP fue uno de los primeros en ser formados en Brasil. En esa época si bien había bastante material, no había un Himno. Yo era el director de Musia en el IAD y un líder del club me pidió que hiciera una música lema para ellos. En ese tiempo, nunca imagine que el Himno se transformaría en una canción oficial a nivel mundial. Tiempo después, la música fue usada para todos los clubes de la Asociación Norte Paranaense, y despues para los clubes da Unión Sur Brasileira, posteriormente para toda la Divisón SudAmericana y hoy és el Himno Oficial para el mundo.
        </p>
        <p>
        <b>Yo Personalmente encuentro muy hermoso el himno de los Aventureros, porque retrata a nuestros pequeños de una forma singular. ¿Qué siente usted cuando los oye cantar el himno?</b>
        </p>
        <p>
        He tenido pocas oportunidades de presenciar a un grupo grande de aventureros cantando el Himno. De cualquier manera, sea un club o una multitud, la sensación es positiva por haber podido ser util para marcar la vida de los niños y lideres con la fidelidad al Señor.
        </p>
        <p>
        <b>Pastor, ¿Qué significa la letra para usted?</b>
        </p>
        <p>
        Para mi describe el compromiso que todos debemos tener en la vida Cristiana. Un compromiso que empieza en la infancia e debe seguir hasta vejez. De nada servirá la fidelidad en la infancia se ella no nos lleva a la eternidad.
        </p>
        <p>
        <b>El himno habla de la confianza en nuestro amigo Jesús, ¿Nos puede explicar algunas de sus frases? </b>
        </p>
        <p>
        <ul>
        <li>1. Confian en Cristo Jesús – Es importante saber que en una época donde las amistades son falsas y vacías y la confianza es un tema en extisión, Jesus es el Amigo en quien podemos confiar.</li>
        <li>2. Reflejar para el mundo su  luz – Todos aquellos que viven con Cristo, participan de su brillo e de Su luz. Somos llamados a ser la luz del mundo, un reflejo de la luz de Cristo.</li>
        <li>3. Maravillas mostrando su amor – No podemos imaginar lo que Dios puede hacer por medio nuestro. Con el, con certeza, veremos e haremos maravillas.</li>
        </ul>
        </p>
